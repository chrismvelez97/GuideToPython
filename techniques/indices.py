#                        How to access an index in a list
list = [1, 2, 3, 4]
print (list[0])  # Note that lists indices start with 0
#   result: 1

#   By using negatives we can actually access the list in a backwards order

#   Ex.1
print (list[-1])
#   result: 4

#   Ex.2
print (list[-2])
#   result: 3
