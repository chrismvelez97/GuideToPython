#                       How to use the values() method

'''
The values() method is attached to the dictionary data type and it returns ONLY
the values within a dictionary.
'''

favorite_languages = {
    "Chris": "Python",
    "Joe": "Swift",
    "Michael": "JavaScript"
}

print (favorite_languages.values())
#   results: dict_values(['JavaScript', 'Swift', 'Python'])
