#                       How to use the keys() method

'''
The keys() method is attached to the dictionary data type and it returns ONLY
the keys within a dictionary.
'''

favorite_languages = {
    "Chris": "Python",
    "Joe": "Swift",
    "Michael": "JavaScript"
}

print (favorite_languages.keys())
#   results: dict_keys(['Michael', 'Chris', 'Joe'])
