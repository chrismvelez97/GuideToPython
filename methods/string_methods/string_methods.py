#                           The .title() method

name = "ada lovelace"
# note that in the name variable ada lovelace is not capitalized
print (name.title())
# The result if you run this is Ada lovelace

#                           The .upper() method
print (name.upper())
# The following line of code should return ADA LOVELACE

#                           The .lower() method
print (name.lower())
# The following line of code should return the variable name lowercased.

#                           The .capitalize() method
myname = "chris"
print (myname.capitalize())
# The following line of code should return "Chris" instead of "chris".
