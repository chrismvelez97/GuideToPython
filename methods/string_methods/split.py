#                       How to use the split() method

'''
The split() function is a function
that analyzes text, and splits every
word into it's own string, and stores
those collections of strings into a
list, temporarily.

In order to save the list you must
store it in a list.

The split() method takes in an
argument that defines where you'd
like the split to occur.
'''
string = "This is a sentence we will split up into a list."
list = string.split()
print (list)
print (string)
